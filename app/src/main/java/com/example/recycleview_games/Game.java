package com.example.recycleview_games;

public class Game {
    String namaGame, publisherGame, deskGame;
    int gambarGame;

    public String getNamaGame() {
        return namaGame;
    }

    public void setNamaGame(String namaGame) {
        this.namaGame = namaGame;
    }

    public String getPublisherGame() {
        return publisherGame;
    }

    public void setPublisherGame(String publisherGame) {
        this.publisherGame = publisherGame;
    }

    public String getDeskGame() {
        return deskGame;
    }

    public void setDeskGame(String deskGame) {
        this.deskGame = deskGame;
    }

    public int getGambarGame() {
        return gambarGame;
    }

    public void setGambarGame(int gambarGame) {
        this.gambarGame = gambarGame;
    }
}
